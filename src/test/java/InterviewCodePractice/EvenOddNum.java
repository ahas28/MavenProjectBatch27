package InterviewCodePractice;

public class EvenOddNum {
	
	
public static void main(String[] args) {
	
	for(int i = 4; i< 11; i++){
	
	if(i % 2 == 1) {
		System.out.println("Even number" + i);
		//print out even number
	}
	else {
		System.out.println("odd number" + i);
		//print out odd numbers
	}
	
}
}
}
